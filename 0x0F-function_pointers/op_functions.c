#include "calc.h"

/**
 * op_add - Performs addition
 * @a: first int to add
 * @b: second int to add
 *
 * Return: a + b (int)
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Performs substraction
 * @a: first int to substract
 * @b: second int to substract
 *
 * Return: a - b (int)
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Performs multiplication
 * @a: first int to multiply
 * @b: second int to multiply
 *
 * Return: a * b (int)
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Performs division
 * @a: first int to divide
 * @b: second int to divide
 *
 * Return: a / b (int)
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Performs mod operation
 * @a: first int to mod
 * @b: second int to mod
 *
 * Return: a % b (int)
 */

int op_mod(int a, int b)
{
	return (a % b);
}
